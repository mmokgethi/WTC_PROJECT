#include "libft.h"

void	ft_putnbr(int n)
{
	char num;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = n * -1;
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
		{
			num = n + '0';
			write(1, &num, 1);
		}
	}
}
